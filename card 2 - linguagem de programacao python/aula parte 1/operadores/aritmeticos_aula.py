# Declaracao e atribuicao
x = 10
y = 3

# Soma: 10 + 3 = 13
print(x + y)

# Subtracao: 10 - 3 = 7
print(x - y)

# Multiplicacao: 10 * 3 = 30
print(x * y)

# Divisao: # 10 / 3 = 3.3333333333333335
print(x / y)

# Módulo (%): Retorna o resto da divisao de 10 por 3.
# 10 dividido por 3 dá 3, e sobra 1. Então imprime: 1
print(x % y)

# Declaracao e atribuicao
par = 34
impar = 33

# Verifica se o resto da divisao por 2 eh zero.
# Como 34 / 2 sobra 0, a comparacao 0 == 0 eh Verdadeira. Imprime: True
print(par % 2 == 0)

# Verifica se o resto da divisao por 2 eh um.
# Como 33 / 2 sobra 1, a comparacao 1 == 1 eh Verdadeira. Imprime: True
print(impar % 2 == 1)
