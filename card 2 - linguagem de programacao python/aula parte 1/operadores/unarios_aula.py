# Declaracao e atribuicao
y = 4

# Operador Lógico 'not': Inverte o valor booleano
print(not False) # Imprime: True
print(not True) # Imprime: False

# Operador de sinal negativo (-): transforma o numero em negativo
print(-3)

# Tambem funciona com variaveis: Inverte o sinal do valor de y (4 vira -4)
print(-y)

# Operador de sinal positivo (+): Na pratica, nao altera o valor numerico.
print(+3)

