# Declaracao e atribuicao
x = 7
y = 5

# 7 eh maior que 5. True
print(x > y)

# 7 eh maior ou igual a 5. True
print(x >= y)

# 7 eh menor que 5. False
print(x < y)

# 7 eh menor ou igual a 5. False
print(x <= y)

# 7 eh igual a 5. False
print(x == y)

# 7 eh diferente de 5. True
print(x != y)

# Comparacao de tipos diferentes: A string '5' eh diferente do numero 5. True
# Para o Python, um texto e um numero nunca sao iguais.
print('5' != 5)