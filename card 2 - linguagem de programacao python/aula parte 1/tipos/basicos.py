# Retorna <class 'int'>: Representa numeros inteiros (positivos, negativos ou zero)
print(type(1))

# Retorna <class 'float'>: Representa numeros decimais ou de "ponto flutuante"
print(type(1.1))

# Retorna <class 'str'>: Abreviação de 'string'. Representa textos ou sequencias de caracteres
print(type('texto'))

# Retorna <class 'bool'>: Representa o tipo Booleano (logica binaria), neste caso, Falso
print(type(False))

# Retorna <class 'bool'>: Também um Booleano, neste caso, representando Verdadeiro
print(type(True))